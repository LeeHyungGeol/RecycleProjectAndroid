package com.example.wasterecycleproject.model;

public class MatchingName {
    private int idx;
    private String cg_name;
    private int cg_middle_idx;
    private String label_name;

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }

    public String getCg_name() {
        return cg_name;
    }

    public void setCg_name(String cg_name) {
        this.cg_name = cg_name;
    }

    public int getCg_middle_idx() {
        return cg_middle_idx;
    }

    public void setCg_middle_idx(int cg_middle_idx) {
        this.cg_middle_idx = cg_middle_idx;
    }

    public String getLabel_name() {
        return label_name;
    }

    public void setLabel_name(String label_name) {
        this.label_name = label_name;
    }
}
